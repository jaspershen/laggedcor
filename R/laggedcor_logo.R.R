#' @title Show the logo laggedcor.
#' @description The laggedcor logo, using ASCII or Unicode characters
#' @author Xiaotao Shen
#' \email{shenxt1990@@outlook.com}
#' @param unicode Whether to use Unicode symbols. Default is `TRUE`
#' on UTF-8 platforms.
#' @return A ASCII log of laggedcor
#' @export
#' @importFrom dplyr filter
#' @importFrom tibble tibble
#' @importFrom rlang is_installed
#' @examples 
#' laggedcor_logo()


##https://onlineasciitools.com/convert-text-to-ascii-art

laggedcor_logo <- function(unicode = l10n_info()$`UTF-8`) {
  
  cat(crayon::green("Thank you for using laggedcor!\n"))
  cat(crayon::green("Version 0.0.1 (2021-12-22)\n"))
  cat(
    crayon::green(
      "More information can be found at https://laggedcor.github.io/laggedcor/\n"
    )
  )
  
  logo =
    c(
      "0 __  _    __   1    2           3  4 ",
      "  _   _     _                               ", " | | (_)   | |                              ",
      " | |_ _  __| |_   _ _ __ ___   __ _ ___ ___ ", " | __| |/ _` | | | | '_ ` _ \\ / _` / __/ __|",
      " | |_| | (_| | |_| | | | | | | (_| \\__ \\__ \\", "  \\__|_|\\__,_|\\__, |_| |_| |_|\\__,_|___/___/",
      "               __/ |                        ", "              |___/                         ",
      "     5  6 /___/      7      8       9 "
    )
  
  
  hexa <- c("*", ".", "o", "*", ".", "*", ".", "o", ".", "*")
  if (unicode)
    hexa <- c("*" = "\u2b22", "o" = "\u2b21", "." = ".")[hexa]
  
  cols <- c(
    "red",
    "yellow",
    "green",
    "magenta",
    "cyan",
    "yellow",
    "green",
    "white",
    "magenta",
    "cyan"
  )
  
  col_hexa <- purrr::map2(hexa, cols, ~ crayon::make_style(.y)(.x))
  
  
  for (i in 0:9) {
    pat <- paste0("\\b", i, "\\b")
    logo <- sub(pat, col_hexa[[i + 1]], logo)
  }
  
  structure(crayon::blue(logo), class = "laggedcor_logo")
}

#' @export

print.laggedcor_logo <- function(x, ...) {
  cat(x, ..., sep = "\n")
  invisible(x)
}



